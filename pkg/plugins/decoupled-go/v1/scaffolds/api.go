// platformHooksTmpl defines the ReconcileHooks interface in the platform layer.
const platformHooksTmpl = `// Code generated by {{ .PluginKey }} ({{ .PluginVersion }}). DO NOT EDIT.
// Regenerate: kubebuilder edit --plugins {{ .PluginKey }} --regenerate
package reconciler

import (
	"context"

	ctrl "sigs.k8s.io/controller-runtime"
	"sigs.k8s.io/controller-runtime/pkg/client"
	"sigs.k8s.io/controller-runtime/pkg/reconcile"
)

// ReconcileHooks defines the extension points for {{ .Kind }} reconciliation.
//
// Implement this interface in internal/app/{{ .KindLower }}/hooks.go.
// The platform reconciler calls these methods in order on each reconcile cycle.
//
// For a no-op starting point, embed NoopHooks in your implementation.
type ReconcileHooks interface {
	// BeforeReconcile is called before the object is fetched from the API server.
	// Use it for pre-flight checks (e.g. leader election guards, rate limiting).
	// Return a non-nil error to skip the main Reconcile step and call OnError.
	BeforeReconcile(ctx context.Context, req reconcile.Request) error

	// Reconcile contains the main business logic for {{ .Kind }}.
	// obj is the current {{ .Kind }} object fetched from the API server.
	// Return (ctrl.Result{}, nil) on success.
	// Return (ctrl.Result{RequeueAfter: d}, nil) to requeue after a delay.
	Reconcile(ctx context.Context, obj client.Object) (ctrl.Result, error)

	// AfterReconcile is called after a successful Reconcile.
	// Use it to update status conditions, emit events, or finalise the cycle.
	AfterReconcile(ctx context.Context, obj client.Object, result ctrl.Result) (ctrl.Result, error)

	// OnError is called when BeforeReconcile or Reconcile returns a non-nil error.
	// Use it to update status conditions and decide whether to requeue.
	// Returning (ctrl.Result{}, nil) suppresses the error from controller-runtime
	// (no exponential backoff). Returning (ctrl.Result{}, err) enables backoff.
	OnError(ctx context.Context, obj client.Object, err error) (ctrl.Result, error)
}
`

