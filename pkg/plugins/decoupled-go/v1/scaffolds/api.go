// APIData holds the data required to render API scaffold templates.
type APIData struct {
	// Group is the API group (e.g. "apps").
	Group string
	// Version is the API version (e.g. "v1").
	Version string
	// Kind is the resource kind (e.g. "MyApp").
	Kind string
	// Namespaced indicates whether the resource is namespace-scoped.
	Namespaced bool
	// PluginKey is the full plugin identifier.
	PluginKey string
	// PluginVersion is the plugin semver string.
	PluginVersion string
}

// KindLower returns the lowercase Kind.
func (d APIData) KindLower() string { return strings.ToLower(d.Kind) }

// Scope returns "Namespaced" or "Cluster" based on Namespaced.
func (d APIData) Scope() string {
	if d.Namespaced {
		return "Namespaced"
	}
	return "Cluster"
}

// RenderAPIPlatform renders platform-zone API templates (always overwritten).
func RenderAPIPlatform(data APIData) (map[string]string, error) {
	rawMap := map[string]string{
		fmt.Sprintf("internal/platform/reconciler/%s_reconciler.go", data.KindLower()): platformReconcilerTmpl,
		"internal/platform/reconciler/hooks.go": platformHooksTmpl,
		"internal/platform/reconciler/noop.go":  platformNoopTmpl,
	}
	return renderMap(rawMap, data)
}

// RenderAPIApp renders app-zone API stub templates (create-only).
func RenderAPIApp(data APIData) (map[string]string, error) {
	rawMap := map[string]string{
		fmt.Sprintf("internal/app/%s/hooks.go", data.KindLower()):      appHooksTmpl,
		fmt.Sprintf("internal/app/%s/hooks_test.go", data.KindLower()): appHooksTestTmpl,
	}
	return renderMap(rawMap, data)
}

