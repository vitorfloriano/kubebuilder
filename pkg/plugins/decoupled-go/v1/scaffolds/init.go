package scaffolds

// InitData holds the data required to render init scaffold templates.
type InitData struct {
	// Domain is the API domain (e.g. "example.com").
	Domain string
	// Repo is the Go module path (e.g. "github.com/myorg/my-operator").
	Repo string
	// ProjectName is the human-readable project name.
	ProjectName string
	// PluginKey is the full plugin identifier (e.g. "decoupled-go.example.com/v1").
	PluginKey string
	// PluginVersion is the plugin semver string (e.g. "v1.0.0").
	PluginVersion string
}

// RenderInit renders all init scaffold templates and returns a map of
// path → file content ready to be added to the plugin response universe.
func RenderInit(data InitData) (map[string]string, error) {
	return renderMap(initPlatformTemplates, data)
}

