package manager

import (
	"flag"
	"os"

	ctrl "sigs.k8s.io/controller-runtime"
	"sigs.k8s.io/controller-runtime/pkg/healthz"
	"sigs.k8s.io/controller-runtime/pkg/log/zap"
	metricsserver "sigs.k8s.io/controller-runtime/pkg/metrics/server"
)

// Options holds the command-line options for the manager.
type Options struct {
	MetricsAddr          string
	EnableLeaderElection bool
	ProbeAddr            string
}

// DefaultOptions returns the default manager options.
func DefaultOptions() Options {
	return Options{
		MetricsAddr:          ":8080",
		EnableLeaderElection: false,
		ProbeAddr:            ":8081",
	}
}

// BindFlags binds manager options to the provided flag set.
func (o *Options) BindFlags(fs *flag.FlagSet) {
	fs.StringVar(&o.MetricsAddr, "metrics-bind-address", o.MetricsAddr,
		"The address the metric endpoint binds to.")
	fs.StringVar(&o.ProbeAddr, "health-probe-bind-address", o.ProbeAddr,
		"The address the probe endpoint binds to.")
	fs.BoolVar(&o.EnableLeaderElection, "leader-elect", o.EnableLeaderElection,
		"Enable leader election for controller manager.")
}

// New creates a new controller manager with standard options.
func New(opts Options) (ctrl.Manager, error) {
	zapOpts := zap.Options{Development: true}
	ctrl.SetLogger(zap.New(zap.UseFlagOptions(&zapOpts)))

	mgr, err := ctrl.NewManager(ctrl.GetConfigOrDie(), ctrl.Options{
		Metrics:                metricsserver.Options{BindAddress: opts.MetricsAddr},
		HealthProbeBindAddress: opts.ProbeAddr,
		LeaderElection:         opts.EnableLeaderElection,
		LeaderElectionID:       "{{ .Domain }}.leader-election",
	})
	if err != nil {
		ctrl.Log.Error(err, "Failed to create manager")
		os.Exit(1)
	}

	if err = mgr.AddHealthzCheck("healthz", healthz.Ping); err != nil {
		ctrl.Log.Error(err, "Failed to set up health check")
		os.Exit(1)
	}
	if err = mgr.AddReadyzCheck("readyz", healthz.Ping); err != nil {
		ctrl.Log.Error(err, "Failed to set up ready check")
		os.Exit(1)
	}

	return mgr, nil
}
